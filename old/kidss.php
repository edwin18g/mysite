<p align="justify" style="margin-bottom: 0.35cm; line-height: 115%"><font face="Castellar, serif"><font size="3" style="font-size: 12pt"><b>MAJOR
ACTIVITIES OF KIDSS</b></font></font></p>
<p align="justify" style="margin-bottom: 0.35cm; line-height: 115%"><font face="Times New Roman, serif"><font size="3" style="font-size: 12pt">There
are ten units functioning under Kuzhithurai Integral Development
Social Services, concentrating the various social ethnic groups and
people engaged in particular profession.</font></font></p>
<p align="justify" style="margin-bottom: 0.35cm; line-height: 115%"><font face="Times New Roman, serif"><font size="3" style="font-size: 12pt"><i><b>Dalit
development Programme (DDP) </b></i></font></font>
</p>
<p align="justify" style="margin-bottom: 0.35cm; line-height: 115%"><font face="Times New Roman, serif"><font size="3" style="font-size: 12pt">From
its inception, KIDSS concentrated more on the problems of the dalits
who are very much discriminated by the high cast people.  The target
people of this programme belong to five communities viz. Barbers,
Dhobis, Kaanis, Pulayars and Sambavars.  Out of the total population
of Dalits 50% are Dhobis, 25% are Barbers and 25% are Sambavars,
Pulayars and Kaanis.  The majority of the people of these communities
are homeless, poverty stricken and leading a very miserable life. </font></font>
</p>
<p align="justify" style="margin-bottom: 0.35cm; line-height: 115%"><font face="Times New Roman, serif"><font size="3" style="font-size: 12pt">The
Dalit Development Programme is taking its maximum effort to develop
the living of these people by way of organizing them into Dalit
Development Units (Association) and giving awareness and empowerment
inputs.  Thus the Dalit Development Program and the target people
have a strong hope that we can try for some daring steps to improve
the status of the target people in respect of social, education and
economic situation. There are 59 groups with 787 members at present</font></font></p>
<p align="justify" style="margin-bottom: 0.35cm; line-height: 115%"><font face="Times New Roman, serif"><font size="3" style="font-size: 12pt"><i><b>Rural
Motivation Programme (RMP)</b></i></font></font></p>
<p align="justify" style="margin-bottom: 0.35cm; line-height: 115%"><font face="Times New Roman, serif"><font size="3" style="font-size: 12pt">Rural
Motivation Program started in the year 2002. Its target people are
agrarian men and women and those who toil with the land.  RMP, as a
unit of KIDSS, has formed these browbeaten people into Sangams
(Village Development Society) and now motivates them to take up
issues and problems affecting the people and community at large.</font></font></p>
<p align="justify" style="margin-bottom: 0.35cm; line-height: 115%"><font face="Times New Roman, serif"><font size="3" style="font-size: 12pt">These
people’s welfare associations encourage and support the illiterate
and the poor to become good leaders and train them to be sustainable
and independent. 119 groups are formed with 3402 members and it has
four people’s Bank.</font></font></p>
<p align="justify" style="margin-bottom: 0.35cm; line-height: 115%"><font face="Times New Roman, serif"><font size="3" style="font-size: 12pt"><i><b>Community
Health Development Programme (CHDP)</b></i></font></font></p>
<p align="justify" style="margin-bottom: 0.35cm; line-height: 115%"><font face="Times New Roman, serif"><font size="3" style="font-size: 12pt">Community
Health Development Programme is spread over 35 villages.  There are
eleven staff members who go to these villages, giving awareness to
the people on health and hygiene.  Recently, a qualified doctor was
appointed to treat the in-patients, who is also in-charge for
conducting camps in the villages.  Unlike the allopathic medicines,
the naturopathy medicines have no side effects.  The Community Health
Development Program also concentrates on the mother’s health,
particularly on their pregnancy period, HIV/AIDS infected people as
well as affected people; promoting capsules like spirulina, preparing
health mix at low price and distributing among the village people.</font></font></p>
<p align="justify" style="margin-bottom: 0.35cm; line-height: 115%"><font face="Times New Roman, serif"><font size="3" style="font-size: 12pt"><i><b>Interior
Fisher-people Organization (IFO)</b></i></font></font></p>
<p align="justify" style="margin-bottom: 0.35cm; line-height: 115%"><font face="Times New Roman, serif"><font size="3" style="font-size: 12pt">The
interior fisher people have their origin at the sea coast; however,
for their livelihood, they purchase fish from coast and sell in the
interior villages, and thus, eventually proceeded to live in the
inland villages.  They do not go for fishing; rather, they are retail
vendors of fish.  Therefore, they are known as Inland Fisher People. 
In actual life, these interior fishermen do not have equal status as
people who live at the sea coast.  </font></font>
</p>
<p align="justify" style="margin-bottom: 0.35cm; line-height: 115%"><font face="Times New Roman, serif"><font size="3" style="font-size: 12pt">KIDSS
tries to organize them into </font></font><font face="Times New Roman, serif"><font size="3" style="font-size: 12pt"><i>Sangams</i></font></font><font face="Times New Roman, serif"><font size="3" style="font-size: 12pt">
(Associations) in order to prevent them from any type of enslavement.
 Right now, there are 50 Sangams at the village level, where people
assemble each month, discuss their problems, resolve to settle the
issues and thus get united in all endeavors.  Acknowledging their
problems for livelihood and human rights, KIDSS has set apart a
separate unit for them, which is known as IFO, that is, Interior
Fisher-people Organization. </font></font>
</p>
<p align="justify" style="margin-bottom: 0.35cm; line-height: 115%"><font face="Times New Roman, serif"><font size="3" style="font-size: 12pt"><i><b>Women
Self Confidence Programme (WSCP)</b></i></font></font></p>
<p align="justify" style="margin-bottom: 0.35cm; line-height: 115%"><font face="Times New Roman, serif"><font size="3" style="font-size: 12pt">The
very concept of widows and their acceptance in the society even in
this era of scientific advancement and technological progress is
dejection, glumness and disheartenment.  They are not supposed to
have rights or needs or self-esteem for themselves.  The outward
appearance itself should mark a difference between the women who have
husbands and widows.</font></font></p>
<p align="justify" style="margin-bottom: 0.35cm; line-height: 115%"><font face="Times New Roman, serif"><font size="3" style="font-size: 12pt">The
widows of Kanyakumari district are working as unskilled laborers in
the following areas: cashew-nut factories, brick making industries,
domestic servants, petty business like flowers, vegetables, fish,
tamarind, coconut, tapioca sellers etc, assisting the masons, making
baskets and scavengers who clean the public toilets and streets.</font></font></p>
<p align="justify" style="margin-bottom: 0.35cm; line-height: 115%"><font face="Times New Roman, serif"><font size="3" style="font-size: 12pt">The
income from these works is very minimal and it is very hard for them
to manage their lives.  There is no job security to these women. 
Many women do not enjoy good health due to various factors.
Hospitalizations and medications throw them into great debts and to
come out of such great money owing is inconceivable to them. </font></font>
</p>
<p align="justify" style="margin-bottom: 0.35cm; line-height: 115%"><font face="Times New Roman, serif"><font size="3" style="font-size: 12pt">Kuzhithurai
Integral Development Social Services has made a statistical study
throughout the project area and identified 1113 widows who are
deserving, helpless and drowning in life due to poverty, abandonment,
sickness and unemployment. We formed 66 Sangams for all these widows.
 </font></font>
</p>
<p align="justify" style="margin-bottom: 0.35cm; line-height: 115%"><font face="Times New Roman, serif"><font size="3" style="font-size: 12pt"><i><b>Banana
Farmers Development Programme (BFDP)</b></i></font></font></p>
<p align="justify" style="margin-bottom: 0.35cm; line-height: 115%"><font face="Times New Roman, serif"><font size="3" style="font-size: 12pt">Banana
Farmers are a sizeable number of workers in our project area.  Most
of them are landless farmers, who get land through lease and then do
banana farming.  The problems of these people are indescribable. 
They need to pay the lease amount for the land received from land
owners, spend money for cultivation, work about 10 months in this
land, and at the end, very often flood or heavy draught cause damages
to the entire banana field. These farmers avail loan from bank or
receive money for interest from private persons throughout these ten
months, and finally lose everything. There are hundreds of suicides
recorded due to such loss resulting great amount of debt. </font></font>
</p>
<p align="justify" style="margin-bottom: 0.35cm; line-height: 115%"><font face="Times New Roman, serif"><font size="3" style="font-size: 12pt">In
2007, Kuzhithurai Integral Development Social Services made an effort
to organize these banana farmers and now we have 147 Sangams in
different villages, which are registered as Banana Farmers Self-Help
Groups. The Unit for these people is called BFDP, that is, Banana
Farmers Development Programme.</font></font></p>
<p align="justify" style="margin-bottom: 0.35cm; line-height: 115%"><font face="Times New Roman, serif"><font size="3" style="font-size: 12pt"><i><b>Vicariate
Integral Development Social Services (VIDSS) </b></i></font></font>
</p>
<p align="justify" style="margin-bottom: 0.35cm; line-height: 115%"><font face="Times New Roman, serif"><font size="3" style="font-size: 12pt">KIDSS
works for the people of six vicariates of the diocese.  Such a
vicariate structure is called VIDSS. VIDSS is operative in our
project area. These VIDSS coordinate the BIDSS at the parish level
that is what we call Basic Integral Development Social Services. 
There are 147 BIDSS in the diocese.  </font></font>
</p>
<p align="justify" style="margin-bottom: 0.35cm; line-height: 115%"><font face="Times New Roman, serif"><font size="3" style="font-size: 12pt">Vicariate
structures have executive committees and separate project directors
to carry out the programme.  All parishes and sub-parishes have
become vibrant missionary centers due to the existing BIDSS
programme.</font></font></p>
<p align="justify" style="margin-bottom: 0.35cm; line-height: 115%"><font face="Times New Roman, serif"><font size="3" style="font-size: 12pt"><i><b>Self
Help Groups (SHG)</b></i></font></font></p>
<p align="justify" style="margin-bottom: 0.35cm; line-height: 115%"><font face="Times New Roman, serif"><font size="3" style="font-size: 12pt">KIDSS
and one of its units “Rural Motivation Programme” have jointly
formed 198 Self Help Groups in this project area. There are three
full-timers and several voluntary workers who organize these groups. 
Except a few, women are the members in all groups.  </font></font>
</p>
