<div class="col-md-8 "><h2><center>Most Rev. V. Jeromedhas, SDB</h2>

<p>
The Most Rev. Msgr. V. Jeromedhas, SDB was born on October 21, 1951 at Paduvoor, Diocese of Kottar. After his primary education in the ICM Primary School, Velliavilai, he joined St. Aloysius' Minor Seminary, Kottar diocese on sunday 24th May 1964, and did his high school studies at Carmel High School, Nagercoil and completed his SSLC in March 1967. He did his P.U.C at Scott Christian College, Nagercoil from June 1967 to March 1968. After two years of Initiation course he joined the Sacred Heart Seminary, Poonamallee, Madras on 12th June 1970.</p> 

<p>After three years of Philosophy Course there, he did his B.A. Economics at Arul Anandar College, Karumathur, Madurai, from July 1973 to March 1976.</p>

<p>It was in 1976 summer he heard the call of Don Bosco within his call and discerned his vocation to be a Salesian and joined the Novitiate in 1977 where he made his first religious profession on 24th May 1978. He made his perpetual profession on 24 May 1981 and thereafter was sent to Salesian Pontifical University, Rome, for his theological formation. He has a Bachelor`s degree in Economics, and a Masters in Pedagogy from the same University. He was ordained a priest for the Salesians of Don Bosco by Pope Saint John Paul II on 2nd June 1985, he returned to India in 1986 and was appointed Vice Rector of Don Bosco Pre-Novitiate at Vellakinar in Coimbatore till 1990 and from then on Rector of the Pre-Novitiate for four years. After his tenure for two years as Dean of students of Theology at AMSAM, Tiruchirappalli, in 1996 he assumed the office of Rector and Parish Priest of St. Mary`s Co-cathedral, Armenian Street Chennai, until 2001 and simultaneously from 1996 to 2001 he served the province of Chennai as one of the Provincial Councillors. From 2003 to 2010 he held the office of the Director of Kalvi Solai, the Department for Coordination of Education and Culture in the province. His seven years of strenuous service at Don Bosco, Thalavadi, was seen as a period of great boon for the tribal mission in the Diocese of Ootacamund, Erode District. From the year 2010 till his appointment as the Bishop of Kuzhithurai, he held the important task of directing the Novices of the Salesian provinces of Chennai and Thiruchirappalli in the capacity of Rector and Director of Novices at Don Bosco, Idaya Deepam, Yelagiri Hills.</p>

<p>Fr. V. Jeromedhas S.D.B. is known for his heart for the poor, simplicity, sharp wisdom, and hard work with a definite purpose. His loving ways with everyone around and especially with the underprivileged bear witness to the gospel values. His gentleness and firmness combine in such a unique manner that he has been thus far a runaway success in every field: formation, social ministry and pastoral care.</p>

<p>The appointment of Fr. V. Jeromedhas S.D.B. as the bishop of the new Diocese of Kuzhithurai is seen by Rev. Fr. Jayapalan, his Provincial, as the best Christmas gift, his province ever received from the Holy See and a bountiful gift in the bicentenary year of Don Bosco’s birth.</p>


